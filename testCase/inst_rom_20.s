	.org 0x0
	.set noat
	.set noreorder
	.set nomacro
	.global _start
_start:
	nop
	lui $1, 0x1e00			#$1=0x1e000000
	ori $1, $1, 0x4			#$1=0x1e000004
	lw $2, 0x0($1)			#$2=[0x1e000004]=0x1023
	lw $3, 0x4($1)			#$3=[0x1e000008]=0xaaaa
	lw $4, 0x8($1)			#$4=[0x1e00000c]=0xbbbb
	lw $5, 0xc($1)			#$5=[0x1e000010]=0xcccc
	lw $6, 0x10($1)			#$6=[0x1e000014]=0x1344
	lw $7, 0x14($1)			#$7=[0x1e000018]=0x0000
	lw $8, 0x18($1)			#$8=[0x1e00001c]=0x1515

_loop:
	j _loop
	nop
