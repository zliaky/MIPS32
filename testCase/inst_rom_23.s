	.org 0x0
	.set noat
	.set noreorder
	.set nomacro
	.global _start
_start:
	nop
	lui $1, 0x1e00			#$1=0x1e000000
	ori $1, $1, 0x4			#$1=0x1e000004
	ori $2, $0, 0x1234 		#$2=0x1234
	ori $3, $0, 0x5678 		#$3=0x5678
	ori $4, $0, 0x90ab 		#$4=0x90ab
	ori $5, $0, 0xcdef 		#$5=0xcdef
	sw $2, 0x0($1)			#[0x1e000004]=0x1234
	sw $3, 0x4($1)			#[0x1e000008]=0x5678
	sw $4, 0x8($1)			#[0x1e00000c]=0x90ab
	sw $5, 0xc($1)			#[0x1e000010]=0xcdef

	lw $6, 0x0($1)
	lw $7, 0x4($1)
	lw $8, 0x8($1)
	lw $9, 0xc($1)

_loop:
	j _loop
	nop
