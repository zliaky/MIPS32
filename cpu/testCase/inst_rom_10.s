.org 0x0
	.set noat
	.set noreorder
	.set nomacro
	.global _start
_start:
	nop
	#第一段：测试sb、lb、lbu指令
	ori $3, $0, 0xeeff		#$3=0x0000eeff
	sb $3, 0x100($0)		#向RAM地址0x100处存储0xff，[0x100]=0xff

	srl $3, $3, 8			#逻辑右移8位，$3=0x000000ee
	sb $3, 0x104($0)		#向RAM地址0x104处存储0xee,[0x104]=0xee

	lb $1, 0x100($0)		#加载0x100处字节并做符号扩展，$1=0xffffffff
	lbu $1, 0x104($0)		#加载0x104处字节并做无符号扩展，$1=0x000000ee

	ori $3, $0, 0x4455		#$3=0x4455
	sll $3, $3, 0x10 		#$3=0x44550000
	ori $3, $3, 0x6677		#$3=0x44556677
	sw $3, 0x110($0)		#向RAM地址0x110处存储0x44556677

	lw $1, 0x110($0)		#加载0x110处的字，$1=0x44556677

	lhu $1, 0x100($0)		#加载0x100处的半字并做无符号扩展，$1=0x000000ff

	nop

_loop:
	j _loop
	nop

